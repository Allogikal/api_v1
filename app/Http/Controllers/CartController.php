    /**
     * Store a newly created resource in storage.
     *
     * @param  int  $id
     */
    public function store($id)
    {
        $user = \auth()->id();
        $product_id = Product::findOrFail($id)->id;

        $cart = Cart::create([
            'user_id' => $user,
            'product_id' => $product_id,
        ]);

        $price = DB::table('carts')
            ->sum('price')->get();

        $response = [
            'cart' => $cart,
            'price' => $price
        ];
        return response($response, 201);
    }

